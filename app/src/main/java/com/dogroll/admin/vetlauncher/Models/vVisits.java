package com.dogroll.admin.vetlauncher.Models;

public class vVisits {
    public int id;
    public String date, client, animal, CR, mAndM, isCD;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getClient() {
        return client;
    }

    public void setClient(String client) {
        this.client = client;
    }

    public String getAnimal() {
        return animal;
    }

    public void setAnimal(String animal) {
        this.animal = animal;
    }

    public String getCR() {
        return CR;
    }

    public void setCR(String CR) {
        this.CR = CR;
    }

    public String getmAndM() {
        return mAndM;
    }

    public void setmAndM(String mAndM) {
        this.mAndM = mAndM;
    }

    public String getIsCD() {
        return isCD;
    }

    public void setIsCD(String isCD) {
        this.isCD = isCD;
    }

    public vVisits(){}
}
